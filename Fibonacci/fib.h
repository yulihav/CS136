// This module provides functions for working with Fibonacci numbers

// fib(n) returns the nth Fibonacci number
int fib(const int n);

// isfib(f) returns 1 if f is a Fibonacci number; otherwise it returns 0
int isfib(const int f);

// fiblow(f, lowtry) returns 1 if f is a Fibonacci number; otherwise it returns 0
int fiblow(const int f, int lowtry);

// fibhigh(f, hightry) returns 1 if f is a Fibonacci number; otherwise it returns 0
int fibhigh(const int f, int hightry);